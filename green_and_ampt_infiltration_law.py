def update_soil_analysis(ec_value, na_value, cec_value):
    # Calculations
    if cec_value > 0:
        esp_val = (na_value / cec_value) * 100
    else:
        esp_val = 0.0

    # Soil Classification Logic
    if ec_value > 4 and esp_val < 15:
        soil_class = "Saline "
    elif ec_value < 4 and esp_val > 15:
        soil_class = "Sodic "
    elif ec_value > 4 and esp_val > 15:
        soil_class = "Saline-Sodic "
    else:
        soil_class = "Normal "

    # Output Display
    print("-" * 40)
    print(f"{'Soil Chemical Analysis':^40}")
    print("-" * 40)
    print(f"{'Soil Class:':<25} {soil_class:>15}")
    print(f"{'ESP (%):':<25} {esp_val:>12.2f}%")
    print(f"{'EC (dS/m):':<25} {ec_value:>13}")
    print("-" * 40)

