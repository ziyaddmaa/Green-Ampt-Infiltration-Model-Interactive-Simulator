# ── Green-Ampt solver ────────────────────────────────────────
def solve_green_ampt(Ks, psi, dtheta, duration_hr, N=300):
    """
    Solves F(t) implicitly via Newton iteration, then derives f(t).

    Governing equation:
        F(t) = Ks*t + psi*dtheta * ln(1 + F(t)/(psi*dtheta))

    Infiltration rate:
        f(t) = Ks * (1 + psi*dtheta / F(t))
    """
    dt = duration_hr / N
    times = np.linspace(0, duration_hr, N + 1)
    F = np.zeros(N + 1)
    f = np.zeros(N + 1)

    f[0] = Ks * (1 + psi * dtheta / 1e-9)   # near-infinite at t=0

    for i in range(1, N + 1):
        F_prev = F[i - 1]
        # Newton-Raphson to solve implicit equation
        F_new = F_prev + Ks * dt              # initial guess
        for _ in range(50):
            lhs = F_new
            rhs = (F_prev + Ks * dt +
                   psi * dtheta * np.log((F_new + psi * dtheta) /
                                         (F_prev + psi * dtheta)))
            F_new = rhs
            if abs(F_new - lhs) < 1e-8:
                break
        F[i] = F_new
        f[i] = Ks * (1 + psi * dtheta / F_new)

    return times, F, f


# ── Plot function ────────────────────────────────────────────
def run_simulation(preset, Ks, psi, ths, thi, rainfall, duration):
    dtheta = max(0.001, ths - thi)
    times, F, f = solve_green_ampt(Ks, psi, dtheta, duration)

    # Cap infiltration rate at rainfall intensity (ponding)
    f_actual = np.minimum(f, rainfall)
    rain_cum  = rainfall * times
    runoff    = np.maximum(0, rain_cum - F)

    # Find ponding time
    ponding_time = None
    for i, fi in enumerate(f):
        if fi <= rainfall:
            ponding_time = times[i]
            break

    # ── Metrics ──
    total_inf    = F[-1]
    total_rain   = rainfall * duration
    total_runoff = max(0, total_rain - total_inf)
    final_rate   = f_actual[-1]

    # ── Figure ──────────────────────────────────────────────
    fig = plt.figure(figsize=(14, 9))
    fig.patch.set_facecolor('#f8f9fa')
    gs = gridspec.GridSpec(2, 3, figure=fig, hspace=0.45, wspace=0.35)

    BLUE   = '#378ADD'
    GREEN  = '#1D9E75'
    ORANGE = '#D85A30'
    GRAY   = '#888780'

    # ── Panel 1: Infiltration rate ───────────────────────────
    ax1 = fig.add_subplot(gs[0, :2])
    ax1.fill_between(times, f_actual, alpha=0.15, color=GREEN)
    ax1.plot(times, f_actual, color=GREEN, lw=2, label='Infiltration rate f(t)')
    ax1.axhline(rainfall, color=BLUE, lw=1.5, ls='--', label=f'Rainfall intensity i = {rainfall:.0f} mm/hr')
    if ponding_time:
        ax1.axvline(ponding_time, color=ORANGE, lw=1.2, ls=':', alpha=0.8,
                    label=f'Ponding at t = {ponding_time:.2f} hr')
    ax1.set_xlabel('Time (hr)', color=GRAY, fontsize=10)
    ax1.set_ylabel('mm / hr', color=GRAY, fontsize=10)
    ax1.set_title('Infiltration rate', fontsize=12, fontweight='normal', color='#2C2C2A')
    ax1.legend(fontsize=9, framealpha=0.5)
    ax1.set_facecolor('#ffffff')
    ax1.tick_params(colors=GRAY)
    ax1.spines['top'].set_visible(False)
    ax1.spines['right'].set_visible(False)

    # ── Panel 2: Soil texture schematic ─────────────────────
    ax2 = fig.add_subplot(gs[0, 2])
    ax2.set_facecolor('#ffffff')
    ax2.set_xlim(0, 1); ax2.set_ylim(0, 1)
    ax2.axis('off')
    ax2.set_title('Soil moisture profile', fontsize=12, fontweight='normal', color='#2C2C2A')

    # Draw soil column with moisture gradient
    n_layers = 20
    wf_depth = min(0.9, F[-1] / max(F[-1], 1) * 0.85 + 0.05)
    for j in range(n_layers):
        depth_frac = j / n_layers
        if depth_frac < wf_depth:
            color = plt.cm.Blues(0.3 + 0.5 * (1 - depth_frac / wf_depth))
        else:
            color = plt.cm.YlOrBr(0.2 + 0.3 * thi / max(ths, 0.001))
        rect = plt.Rectangle((0.2, 1 - (j+1)/n_layers), 0.6, 1/n_layers,
                               facecolor=color, edgecolor='#cccccc', lw=0.3)
        ax2.add_patch(rect)

    ax2.annotate('', xy=(0.5, 1 - wf_depth), xytext=(0.5, 1 - wf_depth - 0.08),
                 arrowprops=dict(arrowstyle='->', color=ORANGE, lw=1.5))
    ax2.text(0.5, 1 - wf_depth - 0.13, 'Wetting front', ha='center',
             fontsize=8, color=ORANGE)
    ax2.text(0.5, 1.02, 'Surface', ha='center', fontsize=8, color=GRAY)
    ax2.text(0.15, 0.92, f'θ = θs\n{ths:.3f}', ha='center', fontsize=7.5, color='#0C447C')
    ax2.text(0.15, 0.35, f'θ = θi\n{thi:.3f}', ha='center', fontsize=7.5, color='#5F5E5A')

    # ── Panel 3: Cumulative ──────────────────────────────────
    ax3 = fig.add_subplot(gs[1, 0])
    ax3.fill_between(times, F, alpha=0.15, color=GREEN)
    ax3.plot(times, F, color=GREEN, lw=2, label='Cumulative F(t)')
    ax3.plot(times, rain_cum, color=BLUE, lw=1.5, ls='--', label='Cumulative rainfall')
    ax3.fill_between(times, F, rain_cum, where=(rain_cum > F), alpha=0.12,
                     color=ORANGE, label='Runoff zone')
    ax3.set_xlabel('Time (hr)', color=GRAY, fontsize=10)
    ax3.set_ylabel('mm', color=GRAY, fontsize=10)
    ax3.set_title('Cumulative infiltration', fontsize=12, fontweight='normal', color='#2C2C2A')
    ax3.legend(fontsize=8, framealpha=0.5)
    ax3.set_facecolor('#ffffff')
    ax3.tick_params(colors=GRAY)
    ax3.spines['top'].set_visible(False)
    ax3.spines['right'].set_visible(False)

    # ── Panel 4: Runoff ──────────────────────────────────────
    ax4 = fig.add_subplot(gs[1, 1])
    ax4.fill_between(times, runoff, alpha=0.15, color=ORANGE)
    ax4.plot(times, runoff, color=ORANGE, lw=2, label='Cumulative runoff')
    ax4.set_xlabel('Time (hr)', color=GRAY, fontsize=10)
    ax4.set_ylabel('mm', color=GRAY, fontsize=10)
    ax4.set_title('Surface runoff', fontsize=12, fontweight='normal', color='#2C2C2A')
    ax4.legend(fontsize=8, framealpha=0.5)
    ax4.set_facecolor('#ffffff')
    ax4.tick_params(colors=GRAY)
    ax4.spines['top'].set_visible(False)
    ax4.spines['right'].set_visible(False)

    # ── Panel 5: Summary metrics ─────────────────────────────
    ax5 = fig.add_subplot(gs[1, 2])
    ax5.set_facecolor('#ffffff')
    ax5.axis('off')
    ax5.set_title('Summary', fontsize=12, fontweight='normal', color='#2C2C2A')

    metrics = [
        ('Final infil. rate',  f'{final_rate:.2f} mm/hr'),
        ('Total infiltration',  f'{total_inf:.1f} mm'),
        ('Total rainfall',      f'{total_rain:.1f} mm'),
        ('Total runoff',        f'{total_runoff:.1f} mm'),
        ('Runoff ratio',        f'{100*total_runoff/max(total_rain,0.001):.1f} %'),
        ('Ponding time',        f'{ponding_time:.3f} hr' if ponding_time else 'None'),
        ('Ks',                  f'{Ks:.1f} mm/hr'),
        ('ψ (suction)',         f'{psi:.1f} mm'),
        ('ΔΘ (moisture def.)', f'{dtheta:.3f}'),
    ]
    for k, (label, val) in enumerate(metrics):
        y = 0.92 - k * 0.10
        ax5.text(0.05, y, label, fontsize=9, color=GRAY, va='top')
        ax5.text(0.98, y, val,   fontsize=9, color='#2C2C2A', va='top',
                 ha='right', fontweight='normal')
        ax5.axhline(y - 0.025, xmin=0.03, xmax=0.97, color='#e0e0e0', lw=0.5)

    fig.suptitle(f'Green-Ampt infiltration  —  {preset}',
                 fontsize=14, fontweight='normal', color='#2C2C2A', y=1.01)
    plt.tight_layout()
    plt.show()


