# Auto-fill sliders when preset changes
def on_preset_change(change):
    p = PRESETS[change['new']]
    w_Ks.value  = p['Ks']
    w_psi.value = p['psi']
    w_ths.value = p['ths']
    w_thi.value = p['thi']

w_preset.observe(on_preset_change, names='value')

out = Output()

def on_change(_):
    with out:
        clear_output(wait=True)
        run_simulation(
            w_preset.value,
            w_Ks.value, w_psi.value,
            w_ths.value, w_thi.value,
            w_rainfall.value, w_duration.value
        )

for w in [w_preset, w_Ks, w_psi, w_ths, w_thi, w_rainfall, w_duration]:
    w.observe(on_change, names='value')

display(widgets.VBox([
    widgets.HTML("<h3 style='margin:0 0 8px;font-weight:500'>Green-Ampt infiltration simulator</h3>"),
    w_preset, w_Ks, w_psi, w_ths, w_thi, w_rainfall, w_duration,
    out
]))

on_change(None)   # draw initial plot
