    py = clay * 0.866

    ax.scatter(px, py, color='red', s=250, zorder=10, edgecolors='black')
    ax.text(px, py + 5, f"{res}\n(Sa:{sand}%, Cl:{clay}%)",
            color='red', fontweight='bold', ha='center', zorder=11, fontsize=12)

    ax.text(-15, -7, 'SAND 100%', fontsize=12, fontweight='bold', color='blue')
    ax.text(85, -7, 'SILT 100%', fontsize=12, fontweight='bold', color='green')
    ax.text(45, 95, 'CLAY 100%', fontsize=12, fontweight='bold', color='red')

    ax.set_title("Soil Texture Result with Grid", fontsize=16, pad=20)
    ax.axis('off')
    ax.set_xlim(-20, 120)
    ax.set_ylim(-15, 110)

    plt.show()

