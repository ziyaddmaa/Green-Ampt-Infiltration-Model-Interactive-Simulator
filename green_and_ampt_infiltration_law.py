def draw_textured_triangle(sand, clay):
    silt = 100 - (sand + clay)

    if clay >= 40: res = "Clay"
    elif clay >= 27: res = "Clay Loam"
    elif sand >= 70: res = "Sandy"
    else: res = "Loam"

    fig, ax = plt.subplots(figsize=(8, 7))

    ticks = np.arange(0, 110, 10)
    for t in ticks:
        # (Sand gridlines - diagonal towards silt)
        ax.plot([t, t/2], [0, t*np.sin(np.pi/3)], color='gray', linestyle='--', linewidth=0.5)
        # (Clay gridlines - horizontal)
        ax.plot([t/2, 100-t/2], [t*np.sin(np.pi/3), t*np.sin(np.pi/3)], color='gray', linestyle='--', linewidth=0.5)
        # (Silt gridlines - diagonal towards sand)
        ax.plot([t, 50+t/2], [0, (100-t)*np.sin(np.pi/3)], color='gray', linestyle='--', linewidth=0.5)

        if t > 0 and t < 100:
            ax.text(t, -4, str(100-t), ha='center', fontsize=8, color='blue')
            ax.text(t/2 - 2, t*np.sin(np.pi/3), str(t), va='center', ha='right', fontsize=8, color='red')
            ax.text(100-t/2 + 2, t*np.sin(np.pi/3), str(100-t), va='center', ha='left', fontsize=8, color='green')

    points = np.array([[0, 0], [100, 0], [50, 86.6], [0, 0]])
    ax.plot(points[:, 0], points[:, 1], color='black', lw=2)

    # x = silt + 0.5 * clay
